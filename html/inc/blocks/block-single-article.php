      <p>
        <a href="#">Although liquid crystals are not really liquid</a>, their molecules behave more like a liquid than they do like a solid, which earns them their name. The crystals in an LCD exist in a kind of a unique middle ground between solid form and liquid form, which gives them the movement and flexibility of a liquid; but can also let them remain in place, like a solid. Heat can quickly melt a solid to liquid, allowing it to move, whereas cool will make the liquid solidify almost instantly. The sensitivity of liquid crystals to temperature can be an advantage, or a disadvantage. It allows for the highly successful use of liquid crystals in devices like thermometers, where temperature responsiveness is a boon; but this same property can unfortunately make LCD screens unreliable in extreme climates.
      </p>

